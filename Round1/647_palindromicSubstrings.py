class Solution:
    def countSubstrings(self, s: str) -> int:
        def countPali(s, left, right):
            res = 0
            while left >= 0 and right < len(s) and s[left] == s[right]:
                res += 1
                left -= 1
                right += 1
            return res
        
        res = 0
        for i in range(len(s)):
            res += countPali(s,i,i)
            res += countPali(s,i,i+1)
        return res
    
